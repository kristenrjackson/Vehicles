public interface Vehicle_I {                // Abstraction: An interface is a 100% abstract class. It can have
    public void fuel();                     // only static, final, and public fields and abstract methods. Also
    public void steering();                 // known as a blueprint of a class. We can implement multiple inheritance
}                                           // in our code, since a class can implement any number of interfaces.
                                            // Classes can access an interface using the "implements" keyword.
